while True:
    num = int(input('Quer mostrar a tabuada de qual número? '))
    
    if num < 0:
        break 
    
    print('\nTabuada do', num)
    for cont in range(11):
        tab = num * cont
        print(f'{num} x {cont} = {tab}')
       
print('\n=-=-=- FIM! -=-=-=')
